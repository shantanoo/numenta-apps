package com.numenta.taurus;

import com.numenta.taurus.instance.InstanceListActivity;

import android.app.Activity;
import android.content.Intent;
import android.os.Bundle;
import android.os.Handler;

public class SplashScreenActivity extends Activity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_splash_screen);
        new Handler().postDelayed(new Runnable() {

            @Override
            public void run() {
                Intent myIntent = new Intent(SplashScreenActivity.this,
                        InstanceListActivity.class);
                startActivity(myIntent);
                finish();
                overridePendingTransition(0, R.anim.fadeout_animation);
            }
        }, 1500);
    }

}
